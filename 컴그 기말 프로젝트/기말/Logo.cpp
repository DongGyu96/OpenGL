void Logo::Render()
{
	glPushMatrix();
	glEnable(GL_TEXTURE_2D);
	glBindTexture(GL_TEXTURE_2D, textures[frame]);
	glColor3f(1, 1, 1);
	glBegin(GL_QUADS); 
	{
		glTexCoord2f(0, 1);
		glVertex2f(800, 0);
		glTexCoord2f(0, 0);
		glVertex2f(800, 800);
		glTexCoord2f(1, 0);
		glVertex2f(0, 800);
		glTexCoord2f(1, 1);
		glVertex2f(0, 0);
	}
	glEnd();

	
	glPopMatrix();
}

