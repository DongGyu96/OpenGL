void Logo::Timer()
{
	int time_control = 20;
	time = (time + 1) % time_control;
	if (time == 0)
	{
		if (frame == 8)
		{
			frame = 7;
			time_control = 5;
		}
		else
			frame = (frame + 1);
	}
	sound.update();
}

void Logo::Relese()
{
	sound.release(1);
}
